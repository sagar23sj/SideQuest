/**
 * Semantic accent colors for the games that don't map cleanly onto a single
 * Material role (Word Guess tile/key states, Spelling Bee feedback). Provided
 * through a [CompositionLocal] so game composables can read theme-aware values
 * via [LocalGameColors] without threading colors through every call.
 */
data class GameColors(
    val guessCorrect: Color,
    val guessPresent: Color,
    val guessAbsent: Color,
)

val LocalGameColors = staticCompositionLocalOf {
    GameColors(
        guessCorrect = GuessCorrectLight,
        guessPresent = GuessPresentLight,
        guessAbsent = GuessAbsentLight,
    )
}

/**
 * The SideQuest theme. Defaults to the brand color scheme so the identity is
 * consistent across devices; dynamic (Material You) color is opt-in via
 * [dynamicColor] rather than the default, which would otherwise override the
 * brand on Android 12+.
 */
@Composable
fun SideQuestTheme(
    darkTheme: Boolean = isSystemInDarkTheme(),
    dynamicColor: Boolean = false,
    content: @Composable () -> Unit,
) {
    val colorScheme = when {
        dynamicColor && Build.VERSION.SDK_INT >= Build.VERSION_CODES.S -> {
            val context = LocalContext.current
            if (darkTheme) dynamicDarkColorScheme(context) else dynamicLightColorScheme(context)
        }

        darkTheme -> DarkColorScheme
        else -> LightColorScheme
    }

    val gameColors = if (darkTheme) {
        GameColors(GuessCorrectDark, GuessPresentDark, GuessAbsentDark)
    } else {
        GameColors(GuessCorrectLight, GuessPresentLight, GuessAbsentLight)
    }

    CompositionLocalProvider(LocalGameColors provides gameColors) {
        MaterialTheme(
            colorScheme = colorScheme,
            typography = Typography,
            shapes = Shapes,
            content = content,
        )
    }
}
